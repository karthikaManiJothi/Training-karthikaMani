package com.exc.cons;

public class SignBit {

	public static void main(String[] args) {
		   int x = -4;
	       System.out.println(x>>1);   
	       int y = 4;
	       System.out.println(y>>1); 
	       int m = -4;
	       System.out.println(m>>>1);   
	       int n = 4;
	       System.out.println(n>>>1); 
	}

}
